// ── Push-registration diagnostics ──────────────────────────────────────────
// A snapshot of what happened during FCM/APNs setup, surfaced on-device by
// long-pressing the app version in Settings. Lets an iOS push failure be
// inspected without a debug console: if `apnsToken` is NULL the device never
// registered with APNs (a signing / provisioning-profile issue); if the APNs
// token is present but `fcmToken` is NULL/error, it's a Firebase-side issue.
class PushDiagnostics {
  static bool    firebaseReady = false;
  static String? permission;
  static String? apnsToken;
  static String? fcmToken;
  static String? error;

  static String _short(String? t) => t == null
      ? 'NULL'
      : '${t.substring(0, t.length < 12 ? t.length : 12)}… (len ${t.length})';

  static String summary() =>
      'Firebase init: ${firebaseReady ? "ready ✓" : "NOT ready ✗"}\n'
      'Notif permission: ${permission ?? "-"}\n'
      'APNs token: ${_short(apnsToken)}\n'
      'FCM token: ${_short(fcmToken)}\n'
      'Error: ${error ?? "none"}';
}

