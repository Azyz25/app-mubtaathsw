// ── High-importance channel so pushes pop as heads-up banners WITH sound ────
// Without an explicit high-importance channel Android files pushes silently in
// the tray ("بشكل خفي جدا"). This id must match the manifest default channel
// meta-data so background/killed messages use it too.
final FlutterLocalNotificationsPlugin _fcmLocalNotifications =
    FlutterLocalNotificationsPlugin();

// One channel per selectable tone. A notification channel's sound is IMMUTABLE
// after creation, so offering several tones means several channels, each bound
// to a bundled raw sound (android/app/src/main/res/raw/tone_*). The backend
// targets one by its id (android.notification.channel_id); the id also feeds the
// manifest default channel so background/killed messages use a real sound too.
// General push channels — stable ids (kept from the previous build so FCM
// notifications are NOT dropped on phones that haven't updated yet). Max
// importance = heads-up banner. Sound plays on the notification stream; if the
// user keeps notification volume at 0 it's silent — that's a device setting,
// separate from delivery. Prayer/adhan (a LOCAL notification, so no channel
// mismatch) is what routes through the alarm stream — see PrayerNotificationService.
const AndroidNotificationChannel _fcmChannel = AndroidNotificationChannel(
